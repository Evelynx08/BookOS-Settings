const DICTS={es:ES,en:EN};

function detectLocale(){
    const stored=(typeof localStorage!=='undefined'&&localStorage.getItem('bookos_lang'))||'';
    if(stored==='es'||stored==='en')return stored;
    if(stored&&stored!=='auto')return stored.startsWith('es')?'es':'en';
    const sys=(navigator.language||navigator.userLanguage||'en').toLowerCase();
    return sys.startsWith('es')?'es':'en';
}

let _lang=detectLocale();

export function getLang(){return _lang;}
export function setLang(lang){
    if(lang==='auto'){localStorage.removeItem('bookos_lang');}
    else{localStorage.setItem('bookos_lang',lang);}
    _lang=detectLocale();
}
export function t(key){
    const d=DICTS[_lang]||EN;
    return d[key]||EN[key]||key;
}
if(typeof window!=='undefined'){window.t=t;window.setLang=setLang;window.getLang=getLang;}
